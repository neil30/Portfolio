const dropdown = document.querySelectorAll(".card-title");
const cardList = document.querySelectorAll("#card-list");

dropdown[0].addEventListener("click", () => {
	if (cardList[0].style.display === "none") {
		cardList[0].style.display = "block";
	} else {
		cardList[0].style.display = "none";
	}
});

